

public class Typecasting {
    public static void main(String[] args) {
        
    int num1 = 23;

    //implicit casting
    double d = num1;
    System.out.println(d);

    // explicit
    double price = 12.99;
    int newprice=(int)price;
    System.out.println("the new price is  "+ newprice);
    }


}
    

